package main

import (
	"fmt"
)

func main() {
	ex1 := findKim([]string{"Queen", "Tod", "Kim"})
	ex2 := findKim([]string{"Gim", "Obama", "xi"})
	fmt.Println(ex1)
	fmt.Println(ex2)
}

func findKim(fnames []string) (ret int) {
	ret = -1
	for i, v := range fnames {
		if v == "Kim" {
			ret = i
		}
	}
	return
}
