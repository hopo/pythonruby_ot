package main

import (
	"fmt"
	"sort"
)

func main() {
	ncard := map[string]int{"김철수": 78, "이하나": 97, "정진원": 88}
	ex1 := sort_dictionary(ncard)
	fmt.Println(ex1)
}

type Ncard struct {
	name   string
	number int
}

func sort_dictionary(nc map[string]int) (ret []Ncard) {
	var slis []string
	for k, _ := range nc {
		slis = append(slis, k)
	}
	sort.Strings(slis)
	for _, k := range slis {
		ret = append(ret, Ncard{k, nc[k]})
	}
	return
}
